package com.huae.bean;

public class Vedio {
	private Integer id;
	private String v_name;
	private String v_img;
	private String v_vedio;
	private String v_path;
	private String carousel;
	private Integer weight;
	
	public String getCarousel() {
		return carousel;
	}
	public void setCarousel(String carousel) {
		this.carousel = carousel;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getV_name() {
		return v_name;
	}
	public void setV_name(String v_name) {
		this.v_name = v_name;
	}
	public String getV_img() {
		return v_img;
	}
	public void setV_img(String v_img) {
		this.v_img = v_img;
	}
	public String getV_vedio() {
		return v_vedio;
	}
	public void setV_vedio(String v_vedio) {
		this.v_vedio = v_vedio;
	}
	public String getV_path() {
		return v_path;
	}
	public void setV_path(String v_path) {
		this.v_path = v_path;
	}
	public Integer getWeight() {
		return weight;
	}
	public void setWeight(Integer weight) {
		this.weight = weight;
	}
}
